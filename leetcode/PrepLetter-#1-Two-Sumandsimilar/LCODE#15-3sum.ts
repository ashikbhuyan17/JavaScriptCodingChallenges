function threeSum(nums: number[]): number[][] {
  nums.sort((a, b) => a - b); // ‡¶ß‡¶æ‡¶™‡ßá 1: ‡¶Ö‡ßç‡¶Ø‡¶æ‡¶∞‡ßá‡¶ü‡¶æ ‡¶õ‡ßã‡¶ü ‡¶•‡ßá‡¶ï‡ßá ‡¶¨‡¶°‡¶º ‡¶∏‡¶æ‡¶ú‡¶æ‡¶ì
  const result = [];
  for (let i = 0; i < nums.length - 2; i++) {
    if (i > 0 && nums[i] === nums[i - 1]) continue; //# Skip duplicate fixed element
    let left = i + 1;
    let right = nums.length - 1;
    while (left < right) {
      const sum = nums[i] + nums[left] + nums[right];
      if (sum == 0) {
        result.push([nums[i], nums[left], nums[right]]);
        left++;
        right--;

        // ‡¶°‡ßÅ‡¶™‡ßç‡¶≤‡¶ø‡¶ï‡ßá‡¶ü ‡¶è‡¶°‡¶º‡¶æ‡¶§‡ßá
        //‚û°Ô∏è left ‡¶™‡¶Ø‡¶º‡ßá‡¶®‡ßç‡¶ü‡¶æ‡¶∞ ‡¶Ø‡¶¶‡¶ø ‡¶Ü‡¶ó‡ßá‡¶∞‡¶ü‡¶æ‡¶∞ ‡¶∏‡¶Æ‡¶æ‡¶® ‡¶π‡¶Ø‡¶º, ‡¶§‡¶æ‡¶π‡¶≤‡ßá ‡¶∏‡ßá‡¶ü‡¶ø‡¶ì skip ‡¶ï‡¶∞‡¶æ ‡¶π‡¶ö‡ßç‡¶õ‡ßá (‡¶°‡ßÅ‡¶™‡ßç‡¶≤‡¶ø‡¶ï‡ßá‡¶ü ‡¶ü‡ßç‡¶∞‡¶ø‡¶™‡¶≤‡ßá‡¶ü ‡¶è‡ßú‡¶æ‡¶®‡ßã‡¶∞ ‡¶ú‡¶®‡ßç‡¶Ø)‡•§
        while (left < right && nums[left] === nums[left - 1]) left++;
        //‚û°Ô∏è right ‡¶™‡¶Ø‡¶º‡ßá‡¶®‡ßç‡¶ü‡¶æ‡¶∞ ‡¶Ø‡¶¶‡¶ø ‡¶Ü‡¶ó‡ßá‡¶∞‡¶ü‡¶æ‡¶∞ ‡¶∏‡¶Æ‡¶æ‡¶® ‡¶π‡¶Ø‡¶º, ‡¶∏‡ßá‡¶ü‡¶æ‡¶ì skip ‡¶ï‡¶∞‡¶æ ‡¶π‡¶ö‡ßç‡¶õ‡ßá‡•§
        while (left < right && nums[right] === nums[right + 1]) right--;
      } else if (sum < 0) {
        left++;
      } else {
        right--;
      }
    }
  }
  return result;
}

